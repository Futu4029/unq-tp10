package encriptacion;

public class ENumerico implements IEncriptador {

	
	public ENumerico() {
		super();
	}
	
	@Override
	public String encriptar(String texto) {
		String resultado = texto;
		return resultado;
	}

	@Override
	public String desencriptar(String texto) {
		String resultado = texto;
		return resultado;
	}

}
